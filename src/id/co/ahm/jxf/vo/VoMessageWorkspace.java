package id.co.ahm.jxf.vo;

/**
 *
 * @author achmad.ha
 */
public class VoMessageWorkspace {
    
    private String f;
    private String m;

    public VoMessageWorkspace() {
    }

    public VoMessageWorkspace(String f, String m) {
        this.f = f;
        this.m = m;
    }
    
    

    public String getF() {
        return f;
    }

    public void setF(String f) {
        this.f = f;
    }

    public String getM() {
        return m;
    }

    public void setM(String m) {
        this.m = m;
    }
    
    
    
}
